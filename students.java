package week8;

public class students {
	// Student.java
	public class Student {
	    private String name;
	    private int age;

	    public Student(String name, int age) {
	        this.name = name;
	        this.age = age;
	    }

	    // Getter methods
	    public String getName() {
	        return name;
	    }

	    public int getAge() {
	        return age;
	    }
	}

}
	
	    


